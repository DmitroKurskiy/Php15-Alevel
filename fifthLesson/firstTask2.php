$arr = ['small string', 'biggest of the string', 'average string'];
function maxstr($a)
{
    $max = [];
    foreach ($a as $key => $item) {
        $max[] .= strlen($item);
        $c = max($max);
    }
    foreach ($a as $item) {
        if (strlen($item) == $c)
            return $item;
        else continue;
    }
}


